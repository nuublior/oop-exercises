// Declare and assign radius values for three circles

double radius1 = 1;
double radius3 = 3;
double radius5 = 5;

// Declare and assign area values for three circles

double area1;
double area3;
double area5;

// Calculate area for each circle

area1 = 3.14159 * radius1 * radius1;
area3 = 3.14159 * radius3 * radius3;
area5 = 3.14159 * radius5 * radius5;

// Display results

Console.WriteLine("The area for the circle of radius " + radius1 + " is " + area1);
Console.WriteLine("The area for the circle of radius " + radius3 + " is " + area3);
Console.WriteLine("The area for the circle of radius " + radius5 + " is " + area5);