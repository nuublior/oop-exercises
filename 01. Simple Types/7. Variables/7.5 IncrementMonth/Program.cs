// Declare an integer variable to represent a month
int month = 2; // February

// Display the current value of the variable
Console.WriteLine("Month value: " + month);

// Increase the value of the variable by 0.5
month += 0.5; // This will cause an error because 'month' is an integer

// Display the value after increasing by 0.5
Console.WriteLine("Month value after adding 0.5: " + month);

// Increase the value again by 0.5
month += 0.5; // Again, this will cause an error

// Display the final value
Console.WriteLine("Final month value: " + month);
